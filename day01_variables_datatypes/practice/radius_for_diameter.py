"""
    Calculating circumference and area of circle by its radius
"""

radius = float(input("Enter radius for the circumference calculation: "))
circumference = 2 * 3.1415926 * radius
area = 3.1415926 * radius * radius
print("Circle Circumference is %.1f" % circumference)
print("Circle Area is %.1f" % area)
